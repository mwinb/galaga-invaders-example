Reflecting on Principle
1) How are Ruby modules (and similar features from other langs)
   different from a class, in their purpose?
   They differ by providing a common functionality between classes, instead of defining a behavior to be specific to one type. 
2) What is LSP?
Liskov's substitution principle says that if a class is inheriting from another class, or has a parent class should be able to be substituted for the parent in all cases.
3) Briefly describe how the template method pattern works.
The template method describes creating abstract methods that while common among classes may have different implementation. While each class may have to "Jump" or "bake" how they go about doing it may differ too much to create a concrete default for all. 
